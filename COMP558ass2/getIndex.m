%Check the angle of an orientation to decide which bin it goes in in the
%histogram
function g = getIndex(angle)
    if(0 <= angle && angle < 10)
        g = 1;
    elseif(10 <= angle && angle < 20)
        g = 2;
    elseif(20 <= angle && angle < 30)
        g = 3;
    elseif(30 <= angle && angle < 40)
        g = 4;
    elseif(40 <= angle && angle < 50)
         g= 5;
    elseif(50 <= angle && angle < 60)
        g= 6;
    elseif(60 <= angle && angle < 70)
        g = 7;
    elseif(70 <= angle && angle < 80)
        g = 8;
    elseif(80 <= angle && angle < 90)
        g= 9;
    elseif(90 <= angle && angle < 100)
        g=10;
    elseif(100 <= angle && angle < 110)
        g=11;
    elseif(110 <= angle && angle < 120)
        g=12;
    elseif(120 <= angle && angle < 130)
        g=13;
    elseif(130 <= angle && angle < 140)
        g = 14;
    elseif(140 <= angle && angle < 150)
        g= 15;
    elseif(150 <= angle && angle < 160)
        g= 16;
    elseif(160 <= angle && angle < 170)
        g = 17;
    elseif(170 <= angle && angle <= 180)
        g= 18;
    elseif(-180 <= angle && angle < -170)
        g = 19;
    elseif(-170 <= angle && angle < -160)
        g=20;
    elseif(-160 <= angle && angle < -150)
        g=21;
    elseif(-150 <= angle && angle < -140)
        g=22;
    elseif(-140 <= angle && angle < -130)
        g=23;
    elseif(-130 <= angle && angle < -120)
        g=24;
    elseif(-120 <= angle && angle < -110)
        g=25;
    elseif(-110 <= angle && angle < -100)
        g=26;
    elseif(-100 <= angle && angle < -90)
        g=27;
    elseif(-90 <= angle && angle < -80)
        g=28;
    elseif(-80 <= angle && angle < -70)
        g=29;
    elseif(-70 <= angle && angle < -60)
        g=30;
    elseif(-60 <= angle && angle < -50)
        g=31;
    elseif(-50 <= angle && angle < -40)
        g=32;
    elseif(-40 <= angle && angle < -30)
        g=33;
    elseif(-30 <= angle && angle < -20)
        g=34;
    elseif(-20 <= angle && angle < -10)
        g= 35;
    elseif(-10 <= angle && angle < 0)
        g=36;
    else
        g = 36;
    end
end